package org.mipams.jumbf.util;

public class JpegXLException extends MipamsException {

    public JpegXLException() {
        super();
    }

    public JpegXLException(String message) {
        super(message);
    }

    public JpegXLException(String message, Throwable cause) {
        super(message, cause);
    }

    public JpegXLException(Throwable cause) {
        super(cause);
    }
}