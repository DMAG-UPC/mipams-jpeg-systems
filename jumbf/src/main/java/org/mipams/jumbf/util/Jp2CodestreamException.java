public class Jp2CodestreamException extends MipamsException {

    public Jp2CodestreamException() {
        super();
    }

    public Jp2CodestreamException(String message) {
        super(message);
    }

    public Jp2CodestreamException(String message, Throwable cause) {
        super(message, cause);
    }

    public Jp2CodestreamException(Throwable cause) {
        super(cause);
    }
}